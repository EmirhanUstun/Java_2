package BankaOrnegi_;

public class CreditCrad {
	
	String no;
	double limit;
	
			
	public CreditCrad(String no, double limit) {
		super();
		this.no = no;
		this.limit = limit;
	}
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public double getLimit() {
		return limit;
	}
	public void setLimit(double limit) {
		this.limit = limit;
	}
}
	
	
	


